// ======================================
// console.log() Examples
// ======================================

// String
console.log("Hello, World!");

// Number
console.log(100);

// Boolean
console.log(true);

// Variable
const name = "Sachin";

console.log(name);

// Multiple values
console.log("Age:", 22);

// Object
const student = {
    name: "Sachin",
    course: "JavaScript"
};

console.log(student);

// Array
const colors = ["Red", "Green", "Blue"];

console.log(colors);

// Current Date
console.log(new Date());

// Button Event
document.getElementById("btn").addEventListener("click", function () {

    console.log("Button Clicked!");

    console.log("Learning console.log()");

});